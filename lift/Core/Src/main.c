void StartDefaultTask(void *argument)
{
  /* USER CODE BEGIN 5 */
	extern unsigned char buttonPressed[3][5];
	extern uint8_t gateFlag;
	extern uint8_t lcdNeedRst;
	F1_UP_LED(0);
	F2_UP_LED(0);
	F3_UP_LED(0);
	F4_UP_LED(0);
	F2_DOWN_LED(0);
	F3_DOWN_LED(0);
	F4_DOWN_LED(0);
	F5_DOWN_LED(0);
	LIFT_F1_LED(0);
	LIFT_F2_LED(0);
	LIFT_F3_LED(0);
	LIFT_F4_LED(0);
  /* Infinite loop */
  for(;;)
  {
	  uint8_t temp=0x00;
	  uint8_t temp0[3]={0x80,0x80,0x80};
	  uint32_t temp1=0;

	  osDelay(100);
	  if(F1_UP_BTN()) {if(getFloor()==1) gateFlag=1;else buttonPressed[1][0]=1;}
	  if(F2_UP_BTN()) {if(getFloor()==2) gateFlag=1;else buttonPressed[1][1]=1;}
	  if(F3_UP_BTN()) {if(getFloor()==3) gateFlag=1;else buttonPressed[1][2]=1;}
	  if(F4_UP_BTN()) {if(getFloor()==4) gateFlag=1;else buttonPressed[1][3]=1;}

	  if(F2_DOWN_BTN()) {if(getFloor()==2) gateFlag=1;else buttonPressed[2][1]=1;}
	  if(F3_DOWN_BTN()) {if(getFloor()==3) gateFlag=1;else buttonPressed[2][2]=1;}
	  if(F4_DOWN_BTN()) {if(getFloor()==4) gateFlag=1;else buttonPressed[2][3]=1;}
	  if(F5_DOWN_BTN()) {if(getFloor()==5) gateFlag=1;else buttonPressed[2][4]=1;}

	  if(LIFT_F1_BTN()) {if(getFloor()==1) gateFlag=1;else buttonPressed[0][0]=1;}
	  if(LIFT_F2_BTN()) {if(getFloor()==2) gateFlag=1;else buttonPressed[0][1]=1;}
	  if(LIFT_F3_BTN()) {if(getFloor()==3) gateFlag=1;else buttonPressed[0][2]=1;}
	  if(LIFT_F4_BTN()) {if(getFloor()==4) gateFlag=1;else buttonPressed[0][3]=1;}
	  if(LIFT_F5_BTN()) {if(getFloor()==5) gateFlag=1;else buttonPressed[0][4]=1;}

	  uint8_t buff[7]={0x7E,0x05,0x41,0x00,0x0B,0x4F,0xEF};//alarm
	  temp=0x01;
	  HAL_UART_Transmit(&huart4,&temp, 1, 100);
	  temp1=getLocation();
	  temp0[2]=0x80+(uint8_t)(temp1%128);
	  temp1=temp1>>7;
	  temp0[1]=0x80+(uint8_t)(temp1%128);
	  temp0[0]=0x80+(uint8_t)(temp1>>7);
	  HAL_UART_Transmit(&huart4,temp0, 3, 100);

	  if(RST_BTN()) {
		  lcdNeedRst=1;
		  temp=0x04;
		  HAL_UART_Transmit(&huart4,&temp, 1, 100);
		  HAL_UART_Transmit(&huart5, buff, 7, 100);
	  }


	  if(ALM_BTN()) {
		  HAL_UART_Transmit(&huart5, buff, 7, 100);
	  }

	  LIFT_F1_LED(buttonPressed[0][0]);
	  LIFT_F2_LED(buttonPressed[0][1]);
	  LIFT_F3_LED(buttonPressed[0][2]);
	  LIFT_F4_LED(buttonPressed[0][3]);
	  LIFT_F5_LED(buttonPressed[0][4]);
	  F1_UP_LED(buttonPressed[1][0]);
	  F2_UP_LED(buttonPressed[1][1]);
	  F3_UP_LED(buttonPressed[1][2]);
	  F4_UP_LED(buttonPressed[1][3]);
	  F2_DOWN_LED(buttonPressed[2][1]);
	  F3_DOWN_LED(buttonPressed[2][2]);
	  F4_DOWN_LED(buttonPressed[2][3]);
	  F5_DOWN_LED(buttonPressed[2][4]);


  }
  /* USER CODE END 5 */
}

